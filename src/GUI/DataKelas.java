    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
         if(inputValid() == true) {
        
        try {
            Class.forName(driver);
            Connection kon = DriverManager.getConnection(database, user, pass);
            
            // Menggunakan PreparedStatement untuk mencegah SQL Injection
            String SQL = "INSERT INTO kelas (kdkelas, nmkelas) VALUES (?, ?)";
            PreparedStatement pstmt = kon.prepareStatement(SQL);
            pstmt.setString(1, tKdKelas.getText());
            pstmt.setString(2, tNmKelas.getText());

            pstmt.executeUpdate();

            // Menyiapkan data untuk ditampilkan di tabel
            data[0] = tKdKelas.getText();
            data[1] = tNmKelas.getText();
           

            // Pastikan variabel 'row' terdefinisi dan memiliki nilai yang benar
            
            tblModel.insertRow(row, data);

            pstmt.close();
            kon.close();
        } catch(SQLException exc) {
            System.err.println("SQL Error: " + exc.getMessage());
        } catch(Exception exc) {
            System.err.println("Error: " + exc.getMessage());
        }
        bersih(); // Pastikan metode bersih() sudah didefinisikan
    }
    }//GEN-LAST:event_bSimpanActionPerformed

    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        if(inputValid()==true){
            try{
           
                    Class.forName(driver);
                    Connection kon=DriverManager.getConnection(database,user,pass);
                    Statement stt=kon.createStatement();
                    String SQL="update kelas set nmkelas='"+tNmKelas.getText()+
                            "' where kdkelas='"+tKdKelas.getText()+"'";
                    
                    stt.executeUpdate(SQL);
                    data[0]=tKdKelas.getText();
                    data[1]=tNmKelas.getText();
                   
                    tblModel.removeRow(row);
                    tblModel.insertRow(row, data);
                    stt.close();
                    kon.close();
                    
            } catch(Exception exc){
                System.err.println(exc.getMessage());
            }
            bersih();
        }
    }//GEN-LAST:event_bUbahActionPerformed

