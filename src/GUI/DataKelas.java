    private boolean inputValid() {
    boolean result;
    
    if (tKdKelas.getText().equals("") || tNmKelas.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Isi data yang dibutuhkan");
        result = false;
    } else {
        result = true;
    }
    
    return result;
}

    
